// Leadership bio expand/collapse functionality
function toggleBio(button) {
    const card = button.closest('.leader-card');
    const preview = card.querySelector('.leader-bio-preview');
    const full = card.querySelector('.leader-bio-full');
    
    if (preview.style.display !== 'none') {
        // Expand
        preview.style.display = 'none';
        full.style.display = 'block';
    } else {
        // Collapse
        preview.style.display = 'block';
        full.style.display = 'none';
    }
}

// Toggle AI Solution Cards
function toggleSolution(card) {
    const details = card.querySelector('.solution-details');
    const expandIcon = card.querySelector('.expand-icon i');
    
    if (details.style.display === 'none') {
        details.style.display = 'block';
        card.classList.add('expanded');
        expandIcon.style.transform = 'rotate(180deg)';
    } else {
        details.style.display = 'none';
        card.classList.remove('expanded');
        expandIcon.style.transform = 'rotate(0deg)';
    }
}

